function [mu_H,mu_L] = find_mu(T)
%Finds dynamic viscosities for liquid components and hydrogen gas 
%PRESSURE INDEPENDANT
%   Inputs: Temperature in Kelvin
%   Outputs: 
%       mu_H: Hydrogen dynamic viscosity
%       mu_L: Vector of liquid dynamic viscosities
%           mu_L=[mu_G;mu_A;mu_P;mu_W;mu_POH]
%--------------------------------------------------------------------------
%REMEMBER THE ORDER: [FH;FG;FA;FP;FW;FPOH]
mu_L=[mu_G(T);mu_A(T);mu_P(T);mu_W(T);mu_POH(T)];
